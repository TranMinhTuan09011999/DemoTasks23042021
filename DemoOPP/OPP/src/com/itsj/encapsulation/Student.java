package com.itsj.encapsulation;

public class Student {
	private int studentId;
    private String name;
    
    public int getId() {
        return studentId;
    }
    public void setId(int id) {
        this.studentId = id;
    }
    public String getname() {
        return name;
    }
    public void setname(String name) {
        this.name = name;
    }
}
